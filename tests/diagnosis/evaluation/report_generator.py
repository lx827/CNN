    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("## 1. 执行摘要")
    lines.append("")
    lines.append("### 1.1 评价范围")
    lines.append("")
    lines.append("本次评价覆盖 `cloud/app/services/diagnosis/` 下的核心算法，包括：")
    lines.append("")
    lines.append("**去噪算法 (5种)**:")
    lines.append("- 小波阈值去噪 (wavelet)")
    lines.append("- VMD变分模态分解 (vmd)")
    lines.append("- 最小熵解卷积 (med)")
    lines.append("- 小波+VMD级联 (wavelet_vmd)")
