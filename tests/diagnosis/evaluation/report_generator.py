    lines.append("# 故障诊断算法全面评价最终报告")
    lines.append("")
    lines.append(f"> 生成时间: {time.strftime('%Y-%m-%d %H:%M:%S')}")
    lines.append("> 评价框架: tests/diagnosis/evaluation/")
    lines.append("> 依据标准: ISO 13374/13379, IEEE TIM, Nature SR 2025")
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("## 目录")
    lines.append("")
    lines.append("1. [执行摘要](#1-执行摘要)")
    lines.append("2. [去噪算法评价](#2-去噪算法评价)")
    lines.append("3. [轴承诊断算法评价](#3-轴承诊断算法评价)")
