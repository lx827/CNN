    lines.append("- 谱相关/谱相干 (sc_scoh)")
    lines.append("")
    lines.append("**齿轮诊断指标 (7种)**:")
    lines.append("- 边频带能量比 (SER)")
    lines.append("- 粗故障指标 (FM0)")
    lines.append("- 局部故障指标 (FM4)")
    lines.append("- 倒频谱幅值比 (CAR)")
